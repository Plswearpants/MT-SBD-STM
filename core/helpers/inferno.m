function cmap = inferno(n)
% INFERNO  Perceptually uniform colormap (port of matplotlib's inferno)
%   cmap = inferno(n)  returns an N-by-3 RGB colormap matrix.
%   cmap = inferno()   returns 256 colors.
%
% Usage:
%   colormap(inferno)
%   colormap(inferno(64))

if nargin < 1
    n = 256;
end

% Key control points sampled from matplotlib's inferno LUT
keyRGB = [ ...
    0.001462, 0.000466, 0.013866; ...
    0.078950, 0.054920, 0.182140; ...
    0.210966, 0.073830, 0.388830; ...
    0.367780, 0.070500, 0.432690; ...
    0.519093, 0.058360, 0.393589; ...
    0.657240, 0.125350, 0.302260; ...
    0.780517, 0.243327, 0.183885; ...
    0.878160, 0.393339, 0.079073; ...
    0.944006, 0.567660, 0.019563; ...
    0.981,    0.751122, 0.143936; ...
    0.987622, 0.913761, 0.401217; ...
    0.988362, 0.998364, 0.644924; ...
];

t_key = linspace(0, 1, size(keyRGB, 1));
t_out = linspace(0, 1, n);

cmap = zeros(n, 3);
for c = 1:3
    cmap(:, c) = interp1(t_key, keyRGB(:, c), t_out, 'pchip');
end
cmap = min(max(cmap, 0), 1);
end
