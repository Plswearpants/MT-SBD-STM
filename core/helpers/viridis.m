function cmap = viridis(n)
% VIRIDIS  Perceptually uniform colormap (port of matplotlib's viridis)
%   cmap = viridis(n)  returns an N-by-3 RGB colormap matrix.
%   cmap = viridis()   returns 256 colors.
%
% Usage:
%   colormap(viridis)
%   colormap(viridis(64))

if nargin < 1
    n = 256;
end

% Key control points sampled from matplotlib's viridis LUT
keyRGB = [ ...
    0.267004, 0.004874, 0.329415; ...
    0.268510, 0.009605, 0.335427; ...
    0.282623, 0.140926, 0.457517; ...
    0.253935, 0.265254, 0.529983; ...
    0.206756, 0.371758, 0.553117; ...
    0.163625, 0.471133, 0.558148; ...
    0.127568, 0.566949, 0.550556; ...
    0.134692, 0.658636, 0.517649; ...
    0.266941, 0.748751, 0.440573; ...
    0.477504, 0.821444, 0.318195; ...
    0.741388, 0.873449, 0.149561; ...
    0.993248, 0.906157, 0.143936; ...
];

t_key = linspace(0, 1, size(keyRGB, 1));
t_out = linspace(0, 1, n);

cmap = zeros(n, 3);
for c = 1:3
    cmap(:, c) = interp1(t_key, keyRGB(:, c), t_out, 'pchip');
end
cmap = min(max(cmap, 0), 1);
end
